    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed
        // TODO add your handling code here:
       try {
        String us = usname.getText().trim();
        String fn = fname.getText().trim();
        String ln = lname.getText().trim();
        char[] passwordArray = password.getPassword();
        String p = new String(passwordArray).trim();
        String securityQuestion = securityQuestionCombo.getSelectedItem().toString();
        String securityAnswer = securityAnswerField.getText().trim();

        // Clear password array
        java.util.Arrays.fill(passwordArray, '0');

        // Validations
        if (us.isEmpty() || fn.isEmpty() || ln.isEmpty() || p.isEmpty() || securityAnswer.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill out all fields including the security answer.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (p.length() < 8) {
            JOptionPane.showMessageDialog(this, "Password must be at least 8 characters long.", "Error", JOptionPane.ERROR_MESSAGE);
            password.setText("");
            return;
        }

        // Username existence check
        pat = con.prepareStatement("SELECT COUNT(*) FROM users WHERE username = ?");
        pat.setString(1, us);
        ResultSet rs = pat.executeQuery();

        if (rs.next() && rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(this, "Username is already taken. Please choose another one.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Gender selection
        String gender = "";
        if (mbutton.isSelected()) gender = "Male";
        else if (fbutton.isSelected()) gender = "Female";

        // Final insert query including security question and answer
        pat = con.prepareStatement("INSERT INTO users (username, password, firstName, lastName, gender, securityQuestion, securityAnswer) VALUES (?, ?, ?, ?, ?, ?, ?)");
        pat.setString(1, us);
        pat.setString(2, p);
        pat.setString(3, fn);
        pat.setString(4, ln);
        pat.setString(5, gender);
        pat.setString(6, securityQuestion);
        pat.setString(7, securityAnswer);

        pat.executeUpdate();

        JOptionPane.showMessageDialog(this, "Thank you for signing up! Your account has been successfully created.");

        // Clear all fields
        usname.setText("");
        fname.setText("");
        lname.setText("");
        password.setText("");
        securityAnswerField.setText("");
        securityQuestionCombo.setSelectedIndex(0); // Reset combo box
        buttonGroup1.clearSelection(); // If you're using a ButtonGroup for radio buttons

    } catch (SQLException ex) {
        Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_kButton1ActionPerformed

