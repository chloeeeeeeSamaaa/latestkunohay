    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed
        // TODO add your handling code here:
        String newPassword = new String(jPasswordField1.getPassword()).trim();
    String confirmPassword = new String(jPasswordField2.getPassword()).trim();

    // ⚠️ Basic validation
    if (newPassword.isEmpty() || confirmPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return;
    }

    if (newPassword.length() < 8) {
        JOptionPane.showMessageDialog(this, "Password must be at least 8 characters long.");
        return;
    }

    if (!newPassword.equals(confirmPassword)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match.");
        return;
    }

    try {
        String updateQuery = "UPDATE users SET password = ? WHERE username = ?";
        PreparedStatement pat = con.prepareStatement(updateQuery);
        pat.setString(1, newPassword);
        pat.setString(2, passedUsername); // username passed from previous JFrame
        int rowsAffected = pat.executeUpdate();

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Password updated successfully.");
            Login ac = new Login();
            ac.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update password.");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Database error occurred.");
        Logger.getLogger(ResetPass.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_kButton1ActionPerformed

